int jenkins_hash( std::string key ) {
	size_t i = 0;
	int hash = 0;
	while ( i != key.length( ) ) {
		hash += key[ i++ ];
		hash += hash << 10;
		hash ^= hash >> 6;
	}
	hash += hash << 3;
	hash ^= hash >> 11;
	hash += hash << 15;
	return hash;
}

OpTable* GetValue_SP( const char* mapname, const char* gametype ) {
	unsigned int MapHash = jenkins_hash( mapname ), GametypeHash = jenkins_hash( gametype );
	for ( int i = 0; i < ARRAYSIZE( Table_SP ); i++ ) {
		if ( Table_SP[ i ].MapHash != MapHash || Table_SP[ i ].GametypeHash != GametypeHash ) continue;
		return &Table_SP[ i ];
	}
	return NULL;
}

